#include "calc.h"

/**
  * op_add - the add function
  * @a: number 1
  * @b: number 2
  *
  * Return: The sum
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - the subtraction function
  * @a: number 1
  * @b: number 2
  *
  * Return: The difference
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - the multiplication function
  * @a: number 1
  * @b: number 2
  *
  * Return: The product
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - the division function
  * @a: number 1
  * @b: number 2
  *
  * Return: The division
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - the remainder of the division  function
  * @a: number 1
  * @b: number 2
  *
  * Return: The remainder
  */
int op_mod(int a, int b)
{
	return (a % b);
}
